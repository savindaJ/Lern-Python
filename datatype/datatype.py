print(type(100)) # int
print(type(100.0)) # float
print(type("100")) # str
print(type(True)) # bool
print(type(100j)) # complex j is imaginary number
print(type([1, 2, 3])) # list
print(type((1, 2, 3))) # tuple
print(type({1, 2, 3})) # set
print(type({1: "one", 2: "two", 3: "three"})) # dict
print('welcome to \n python world !') # new line character / escape character
print('welcome to \t python world !') # tab character / escape character
print('welcome to \b\b python world !') # backspace character / escape character
print("hello World !\" hello")
print("hello World !\' hello")
print("hello World !\\ hello")